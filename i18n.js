function getPage() {
  const path = location.pathname;
  if (path.endsWith("about.html")) return "about";
  if (path.endsWith("research.html")) return "research";
  if (path.endsWith("ta.html")) return "ta";
  if (path.endsWith("education.html")) return "education";
  if (path.endsWith("cv.html")) return "cv";
  return "home";
}

function getSavedLang() {
  try {
    return localStorage.getItem("lang") || "en";
  } catch {
    return "en";
  }
}

function saveLang(lang) {
  try {
    localStorage.setItem("lang", lang);
  } catch {}
}

function applyTranslation(lang) {
  const t = i18n[lang];
  const page = getPage();

  document.documentElement.lang = lang === "zh" ? "zh-CN" : lang;

  // Nav
  document.querySelectorAll("[data-i18n-nav]").forEach((el) => {
    const key = el.getAttribute("data-i18n-nav");
    if (t.nav[key]) el.textContent = t.nav[key];
  });

  // Generic textContent keys
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const val = key.split(".").reduce((o, k) => o?.[k], t);
    if (val !== undefined) {
      if (val === "") {
        el.style.display = "none";
      } else {
        el.style.display = "";
        el.textContent = val;
      }
    }
  });

  // innerHTML keys (content that may contain HTML tags)
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    const val = key.split(".").reduce((o, k) => o?.[k], t);
    if (val !== undefined) el.innerHTML = val;
  });

  // Update switcher active state
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });
}

function switchLang(lang) {
  saveLang(lang);
  applyTranslation(lang);
}

// Init
document.addEventListener("DOMContentLoaded", () => {
  // Build the language switcher
  const switcher = document.createElement("div");
  switcher.className = "lang-switcher";
  switcher.setAttribute("data-no-translate", "");
  const langs = [
    { code: "en", label: "EN" },
    { code: "zh", label: "中文" },
    { code: "fr", label: "FR" },
  ];
  langs.forEach(({ code, label }) => {
    const btn = document.createElement("button");
    btn.className = "lang-btn";
    btn.dataset.lang = code;
    btn.textContent = label;
    btn.addEventListener("click", () => switchLang(code));
    switcher.appendChild(btn);
  });
  document.body.appendChild(switcher);

  applyTranslation(getSavedLang());
});
