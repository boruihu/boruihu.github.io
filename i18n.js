function getPage() {
  const path = location.pathname;
  if (path.endsWith("about.html")) return "about";
  if (path.endsWith("research.html")) return "research";
  if (path.endsWith("ta.html")) return "ta";
  if (path.endsWith("education.html")) return "education";
  if (path.endsWith("cv.html")) return "cv";
  if (path.endsWith("misc.html")) return "misc";
  if (path.endsWith("life.html")) return "life";
  return "home";
}

function getSavedLang() {
  try {
    return localStorage.getItem("lang") || "en";
  } catch {
    return "en";
  }
}

function saveLang(lang) {
  try {
    localStorage.setItem("lang", lang);
  } catch {}
}

function applyTranslation(lang) {
  const t = i18n[lang];
  const page = getPage();

  document.documentElement.lang = lang === "zh" ? "zh-CN" : lang;

  // Page title
  const pageTitle = t[page]?.title;
  if (pageTitle) document.title = pageTitle;

  // Nav
  document.querySelectorAll("[data-i18n-nav]").forEach((el) => {
    const key = el.getAttribute("data-i18n-nav");
    if (t.nav[key]) el.textContent = t.nav[key];
  });

  // Generic textContent keys
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const val = key.split(".").reduce((o, k) => o?.[k], t);
    if (val !== undefined) {
      if (val === "") {
        el.style.display = "none";
      } else {
        el.style.display = "";
        el.textContent = val;
      }
    }
  });

  // innerHTML keys (content that may contain HTML tags)
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    const val = key.split(".").reduce((o, k) => o?.[k], t);
    if (val !== undefined) {
      if (val === "") {
        el.style.display = "none";
      } else {
        el.style.display = "";
        el.innerHTML = val;
      }
    }
  });

  // Update switcher active state
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });
}

function switchLang(lang) {
  saveLang(lang);
  applyTranslation(lang);
}

// Dark mode
function initTheme() {
  const saved = localStorage.getItem("dark");
  if (saved !== null) {
    document.documentElement.classList.toggle("dark", saved === "true");
  } else if (window.matchMedia("(prefers-color-scheme: dark)").matches) {
    document.documentElement.classList.add("dark");
  }
  updateDarkBtn();
}

function toggleDark() {
  document.documentElement.classList.toggle("dark");
  try {
    localStorage.setItem("dark", document.documentElement.classList.contains("dark"));
  } catch {}
  updateDarkBtn();
}

function updateDarkBtn() {
  const btn = document.querySelector(".dark-btn");
  if (!btn) return;
  btn.textContent = document.documentElement.classList.contains("dark") ? "☀" : "☾";
}

